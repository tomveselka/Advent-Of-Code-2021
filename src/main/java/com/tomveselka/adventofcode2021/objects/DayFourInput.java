package com.tomveselka.adventofcode2021.objects;

import java.util.ArrayList;
import java.util.List;

public class DayFourInput {
	private ArrayList<List<String>> numbers = new ArrayList<List<String>>();
	private ArrayList<List<String>> states = new ArrayList<List<String>>();
	public ArrayList<List<String>> getNumbers() {
		return numbers;
	}
	public void setNumbers(ArrayList<List<String>> numbers) {
		this.numbers = numbers;
	}
	public ArrayList<List<String>> getStates() {
		return states;
	}
	public void setStates(ArrayList<List<String>> states) {
		this.states = states;
	}

}
